func TestRunHonorsBlockingAndDisabledStatus(t *testing.T) {
	t.Parallel()

	tests := map[string]struct {
		status          string
		wantDiagnostics int
		wantBlocking    bool
	}{
		"blocking": {status: "blocking", wantDiagnostics: 1, wantBlocking: true},
		"advisory": {status: "advisory", wantDiagnostics: 1},
		"disabled": {status: "disabled"},
	}
	for name, test := range tests {
		test := test
		t.Run(name, func(t *testing.T) {
			t.Parallel()
			root := writeModule(t, map[string]string{
				"sample/sample.go": "package sample\nimport _ \"unsafe\"\n",
			})
			config := writePolicy(t, root, "version: 1\nrules:\n"+
				"  security/no-unsafe:\n    status: "+test.status+"\n")
			result, err := driver.Run(context.Background(), driver.Options{
				ConfigPath: config,
				Patterns:   []string{"./..."},
				Sequential: true,
			})
			if err != nil {
				t.Fatalf("Run() error = %v", err)
			}
			if len(result.Report.Diagnostics) != test.wantDiagnostics ||
				result.Blocking != test.wantBlocking {
				t.Fatalf("Run() diagnostics = %d, blocking = %t",
					len(result.Report.Diagnostics), result.Blocking)
			}
			if len(result.Report.Diagnostics) > 0 &&
				filepath.IsAbs(result.Report.Diagnostics[0].Filename) {
				t.Fatal("Run() exposed an absolute diagnostic path")
			}
		})
	}
}

