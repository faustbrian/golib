func TestRunIsDeterministicAcrossConcurrency(t *testing.T) {
	t.Parallel()

	root := writeModule(t, map[string]string{
		"sample/sample.go": `package sample
import (
    "context"
    _ "unsafe"
)
var Root = context.Background()
`,
	})
	config := writePolicy(t, root, `version: 1
exceptions:
  - rule: security/no-unsafe
    package: example.com/fixture/sample
    path: sample/sample.go
    reason: deterministic reviewed exception
`)
	now := time.Date(2026, 7, 16, 12, 0, 0, 0, time.UTC)
	baseline, err := driver.Run(context.Background(), driver.Options{
		ConfigPath: config,
		Patterns:   []string{"./..."},
		Sequential: true,
		Now:        now,
	})
	if err != nil {
		t.Fatalf("Run(sequential) error = %v", err)
	}
	want := reportJSON(t, baseline.Report)
	for iteration := 0; iteration < 5; iteration++ {
		result, err := driver.Run(context.Background(), driver.Options{
			ConfigPath: config,
			Patterns:   []string{"./..."},
			Now:        now,
		})
		if err != nil {
			t.Fatalf("Run(concurrent %d) error = %v", iteration, err)
		}
		if got := reportJSON(t, result.Report); !bytes.Equal(got, want) {
			t.Fatalf("concurrent report %d differs\n got: %s\nwant: %s",
				iteration, got, want)
		}
	}
}

func reportJSON(t *testing.T, report shared.Report) []byte {
	t.Helper()

	var output bytes.Buffer
	if err := shared.WriteJSON(&output, report); err != nil {
		t.Fatalf("WriteJSON() error = %v", err)
	}
	return output.Bytes()
}

func writeModule(t *testing.T, files map[string]string) string {
	t.Helper()

	root := t.TempDir()
	files["go.mod"] = "module example.com/fixture\n\ngo 1.26.0\n"
	for name, contents := range files {
		path := filepath.Join(root, filepath.FromSlash(name))
		if err := os.MkdirAll(filepath.Dir(path), 0o700); err != nil {
			t.Fatalf("MkdirAll() error = %v", err)
		}
		if err := os.WriteFile(path, []byte(contents), 0o600); err != nil {
			t.Fatalf("WriteFile() error = %v", err)
		}
	}

	return root
}

func writePolicy(t *testing.T, root, contents string) string {
	t.Helper()

	contents = strings.ReplaceAll(
		contents,
		"    status: blocking\n",
		"    status: blocking\n"+
			"    promotion:\n"+
			"      version: 0.1.0\n"+
			"      evidence: driver integration fixture\n",
	)
	path := filepath.Join(root, "analysis.yml")
	if err := os.WriteFile(path, []byte(contents), 0o600); err != nil {
		t.Fatalf("WriteFile() error = %v", err)
	}

	return path
}
